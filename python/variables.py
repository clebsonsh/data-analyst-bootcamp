# variable declaration
# python infer variable type
# in this case we have a int
x = 22

# print x in the terminal
print(x)

# print x type in the terminal
print(type(x))

# assign a string to y
y = "Mint Chocolate Chip"

# print y in the terminal
print(y)

# print y type in the terminal
print(type(y))

# assign another string to y
y = "Chocolate"

# print y new value in the terminal
print(y)

# we can also assign multiple values to multiple variables
x, y, z = "Chocolate", "Vanilla", "Rocky Road"

# print x, y, and z values in the terminal
print(x)
print(y)
print(z)

# we can also assign one value to multiple variables
x = y = z = "Root Beer Float"

# print x, y, and z values in the terminal
print(x)
print(y)
print(z)

# assign a list to ice_creams
ice_creams = ["Chocolate", "Vanilla", "Rocky Road"]

# we can take the values from a list and assign to multiple variables
x, y, z = ice_creams

# print x, y, and z values in the terminal
print(x)
print(y)
print(z)

